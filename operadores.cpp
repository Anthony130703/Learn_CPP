#include <iostream>

int main()
{
    //Aritmeticos
    //suma -> a + b 
    // 5 + 2

    std::cout << "(5 + 2) = " << (5 + 2) << std::endl;

    //suma -> a - b 
    // 5 - 2
    
    std::cout << "(5 - 2) = " << (5 - 2) << std::endl;

    //suma -> a * b 
    // 5 * 2
    
    std::cout << "(5 * 2) = " << (5 * 2) << std::endl;

    //suma -> a / b 
    // 5 / 2
    
    std::cout << "(5 / 2) = " << (5 / 2) << std::endl;

    //suma -> a % b 
    // 5 % 2
    
    std::cout << "(5 % 2) = " << (5 % 2) << std::endl;

    return 0;
}